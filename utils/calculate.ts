export const add = (first: number, second: number) => {
  return first + second;
};

export const subtract = (first: number, second: number) => {
  return first - second;
};

export const multiply = (first: number, second: number) => {
  return first * second;
};

export const divide = (first: number, second: number) => {
  return first / second;
};
